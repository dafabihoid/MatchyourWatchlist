class Media {
  final int id;
  final String title;
  final String genres;
  final String providers;
  final String description;
  final String cover;
  final String mediaType;

  const Media({
    required this.id,
    required this.title,
    required this.genres,
    required this.providers,
    required this.description,
    required this.cover,
    required this.mediaType,
  });
}
